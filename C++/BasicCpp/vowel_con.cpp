        break;
    case 'i':
        cout<<"'i' is a vowel";
        break;
    case 'o':
        cout<<"'o' is a vowel";
        break;
    case 'u':
        cout<<"'u' is a vowel";
        break;
    case 'A':
        cout<<"'A' is a vowel";
        break;
    case 'E':
        cout<<"'E' is a vowel";
        break;
    case 'I':
        cout<<"'I' is a vowel";
